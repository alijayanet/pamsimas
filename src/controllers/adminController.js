async function sendQrisReminderBulk(req, res) {
  const bulan = Number(req.body.bulan || 0);
  const tahun = Number(req.body.tahun || 0);
  if (!bulan || !tahun) {
    req.flash('danger', 'Periode tidak valid.');
    return res.redirect('/admin/whatsapp');
  }

  const bills = tagihanModel.listUnpaidByPeriod(bulan, tahun);
  let sentCount = 0;
  let failCount = 0;

  for (const bill of bills) {
    try {
      const ok = await whatsappService.sendQrisPaymentRequest(bill);
      if (ok) {
        sentCount += 1;
      } else {
        failCount += 1;
      }
    } catch (error) {
      failCount += 1;
    }
  }

  req.flash('success', `Reminder QRIS selesai. Berhasil: ${sentCount}, Gagal: ${failCount}, Total: ${bills.length}.`);
  return res.redirect('/admin/whatsapp');
}

