function listTagihan(req, res) {
  const now = dayjs();
  const bulan = Number(req.query.bulan || (now.month() + 1));
  const tahun = Number(req.query.tahun || now.year());
  const status = String(req.query.status || 'all');
  const keyword = String(req.query.keyword || '');
  const limit = Number(req.query.limit || 200);

  const settings = pengaturanModel.getSettings();
  const bills = tagihanModel.listByPeriod({ bulan, tahun, status, keyword, limit });
  const summary = bills.reduce(
    (acc, item) => {
      acc.total += 1;
      if (item.status_bayar === 'lunas') {
        acc.lunas += 1;
      } else {
        acc.belum += 1;
      }
      return acc;
    },
    { total: 0, lunas: 0, belum: 0 }
  );

  return res.render('admin/tagihan-index', {
    title: 'Tagihan / Invoice',
    bulan,
    tahun,
    status,
    keyword,
    limit,
    summary,
    bills,
    billingSettings: settings,
    collectionWindow: getCollectionWindow(settings),
    autoGenerateDay: getAutoGenerateDay(settings),
    generationDefaults: getGenerationDefaults()
  });
}

