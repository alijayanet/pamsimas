async function markBillPaid(req, res) {
  const tagihan = tagihanModel.getByIdDetail(req.params.id);
  if (!tagihan) {
    req.flash('danger', 'Tagihan tidak ditemukan.');
    return res.redirect('/admin');
  }

  if (tagihan.status_bayar === 'lunas') {
    req.flash('warning', 'Tagihan sudah berstatus lunas.');
    return res.redirect('/admin');
  }

  tagihanModel.markPaid(tagihan.id, req.body.metode_bayar || 'manual');
  keuanganModel.createEntry({
    tipe: 'pemasukan',
    jumlah: tagihan.total_tagihan,
    keterangan: `Pembayaran tagihan ${tagihan.nama} periode ${tagihan.bulan}/${tagihan.tahun}`
  });
  pembayaranLogModel.create({
    tagihan_id: tagihan.id,
    user_id: req.session.user.id,
    jumlah_bayar: tagihan.total_tagihan,
    metode_bayar: req.body.metode_bayar || 'manual',
    keterangan: 'Pelunasan dari panel admin'
  });

  const updatedBill = tagihanModel.getByIdDetail(tagihan.id);
  await whatsappService.sendPaymentReceipt(updatedBill);

  req.flash('success', 'Tagihan berhasil ditandai lunas.');
  return res.redirect('/admin');
}

