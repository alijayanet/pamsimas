function updatePelanggan(req, res) {
  const current = pelangganModel.getById(req.params.id);
  if (!current) {
    req.flash('danger', 'Data pelanggan tidak ditemukan.');
    return res.redirect('/admin/pelanggan');
  }

  try {
    const { no_meteran, nama, alamat, no_whatsapp, tgl_bergabung, username, password } = req.body;
    let userId = current.user_id;

    if (username) {
      if (!userId && !password) {
        req.flash('danger', 'Password wajib diisi saat pertama kali membuat login pelanggan.');
        return res.redirect(`/admin/pelanggan/${current.id}/edit`);
      }

      userId = userModel.upsertPelangganLogin({
        userId,
        username,
        password
      });
    }

    pelangganModel.update(current.id, {
      no_meteran,
      nama,
      alamat,
      no_whatsapp,
      tgl_bergabung,
      user_id: userId
    });

    req.flash('success', 'Data pelanggan berhasil diperbarui.');
    return res.redirect('/admin/pelanggan');
  } catch (error) {
    req.flash('danger', error.message);
    return res.redirect(`/admin/pelanggan/${current.id}/edit`);
  }
}

