function getGenerationDefaults(referenceDate = dayjs()) {
  const billingPeriod = {
    bulan: referenceDate.month() + 1,
    tahun: referenceDate.year()
  };
  const sourcePeriod = getPreviousPeriod(billingPeriod.bulan, billingPeriod.tahun);
  const nextPeriod = getNextPeriod(billingPeriod.bulan, billingPeriod.tahun);

  return {
    sourcePeriod,
    billingPeriod,
    nextPeriod
  };
}

function getAdminRedirectTarget(value, fallback = '/admin') {
  if (typeof value !== 'string') {
    return fallback;
  }

  const trimmed = value.trim();
  if (!trimmed.startsWith('/admin')) {
    return fallback;
  }

  return trimmed;
}

