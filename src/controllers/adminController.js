async function retryQrisLog(req, res) {
  const log = qrisNotifLogModel.getById(req.params.id);
  const appConfig = aplikasiModel.getSettings();

  if (!log) {
    req.flash('danger', 'Log QRIS tidak ditemukan.');
    return res.redirect('/admin/pengaturan');
  }

  if (!log.amount_detected) {
    req.flash('warning', 'Log ini tidak memiliki nominal yang bisa dicocokkan ulang.');
    return res.redirect('/admin/pengaturan');
  }

  if (log.match_status === 'matched') {
    req.flash('info', 'Log ini sudah pernah berhasil dicocokkan.');
    return res.redirect('/admin/pengaturan');
  }

  const result = await processQrisAmount(log.amount_detected, appConfig.qris_match_window_minutes);
  if (!result.matched) {
    req.flash('warning', 'Retry match belum menemukan tagihan yang sesuai.');
    return res.redirect('/admin/pengaturan');
  }

  qrisNotifLogModel.finish({
    payload_hash: log.payload_hash,
    amount_detected: log.amount_detected,
    message_text: log.message_text,
    sender: log.sender,
    matched_tagihan_id: result.bill.id,
    match_status: result.match_status,
    match_reason: 'manual_retry_match'
  });

  req.flash('success', `Log QRIS berhasil dicocokkan ke tagihan ${result.bill.no_meteran}.`);
  return res.redirect('/admin/pengaturan');
}

