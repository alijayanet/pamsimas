async function generateBillsManual(req, res) {
  const redirectTarget = getAdminRedirectTarget(req.body.redirect_to, '/admin');
  const sourceMonth = Number(req.body.source_month || 0);
  const sourceYear = Number(req.body.source_year || 0);
  const billingMonthInput = Number(req.body.billing_month || 0);
  const billingYearInput = Number(req.body.billing_year || 0);

  if (!sourceMonth || sourceMonth < 1 || sourceMonth > 12 || !sourceYear) {
    req.flash('danger', 'Periode pencatatan tidak valid.');
    return res.redirect(redirectTarget);
  }

  const computedBilling = getNextPeriod(sourceMonth, sourceYear);
  const billingMonth = billingMonthInput >= 1 && billingMonthInput <= 12 ? billingMonthInput : computedBilling.bulan;
  const billingYear = billingYearInput || computedBilling.tahun;

  const result = await generateBillsForReadingPeriod({
    sourceMonth,
    sourceYear,
    billingMonth,
    billingYear,
    generatedMode: 'manual'
  });

  if (!result.totalCandidates) {
    req.flash('warning', `Tidak ada data pencatatan periode ${sourceMonth}/${sourceYear} yang siap digenerate.`);
    return res.redirect(redirectTarget);
  }

  const failureText = result.failed.length ? ` Gagal: ${result.failed.length}.` : '';
  req.flash(
    'success',
    `Generate tagihan manual ${sourceMonth}/${sourceYear} ke periode tagihan ${billingMonth}/${billingYear} selesai. Dibuat: ${result.created}, Skip: ${result.skipped}, QRIS siap: ${result.qrisPrepared}, WA terkirim: ${result.waSent}.${failureText}`
  );
  return res.redirect(redirectTarget);
}

